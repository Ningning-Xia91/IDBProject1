package happyH.models;

public class Restaurant {

	private String r_id;
	private String r_name;
	private String r_details;
	private int ave_price;
	private String ct_id;
	
	public String getR_id() {
		return r_id;
	}
	public void setR_id(String r_id) {
		this.r_id = r_id;
	}
	public String getR_name() {
		return r_name;
	}
	public void setR_name(String r_name) {
		this.r_name = r_name;
	}
	public String getR_details() {
		return r_details;
	}
	public void setR_details(String r_details) {
		this.r_details = r_details;
	}
	public int getAve_price() {
		return ave_price;
	}
	public void setAve_price(int ave_price) {
		this.ave_price = ave_price;
	}
	public String getCt_id() {
		return ct_id;
	}
	public void setCt_id(String ct_id) {
		this.ct_id = ct_id;
	}
	
	
}
