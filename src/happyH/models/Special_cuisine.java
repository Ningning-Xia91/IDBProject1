package happyH.models;

public class Special_cuisine {
	
	private int sc_id;
	private String sc_name;
	private String sc_details;
	private int sc_price;
	private int r_id;
	public int getSc_id() {
		return sc_id;
	}
	public void setSc_id(int sc_id) {
		this.sc_id = sc_id;
	}
	public String getSc_name() {
		return sc_name;
	}
	public void setSc_name(String sc_name) {
		this.sc_name = sc_name;
	}
	public String getSc_details() {
		return sc_details;
	}
	public void setSc_details(String sc_details) {
		this.sc_details = sc_details;
	}
	
	public int getSc_price() {
		return sc_price;
	}
	public void setSc_price(int sc_price) {
		this.sc_price = sc_price;
	}
	public int getR_id() {
		return r_id;
	}
	public void setR_id(int r_id) {
		this.r_id = r_id;
	}
	
	

}
