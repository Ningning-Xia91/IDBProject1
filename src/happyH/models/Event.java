package happyH.models;

public class Event {
	
	private String evt_id;
	private String r_id;
	private String evt_title;
	private String start_time;
	private String end_time;
	private String e_details;
	
	public String getEvt_id() {
		return evt_id;
	}
	public void setEvt_id(String evt_id) {
		this.evt_id = evt_id;
	}
	
	public String getR_id() {
		return r_id;
	}
	public void setR_id(String r_id) {
		this.r_id = r_id;
	}
	public String getEvt_title() {
		return evt_title;
	}
	public void setEvt_title(String evt_title) {
		this.evt_title = evt_title;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public String getEnd_time() {
		return end_time;
	}
	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}
	public String getE_details() {
		return e_details;
	}
	public void setE_details(String e_details) {
		this.e_details = e_details;
	}
	
	

}
